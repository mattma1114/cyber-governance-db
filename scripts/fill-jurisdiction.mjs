function inferJurisdiction(id, hq, name) {
  // Check override first
  if (PLATFORM_OVERRIDES[id]) return PLATFORM_OVERRIDES[id];

  const h = (hq || name || "").toLowerCase();
  const result = [];

  // Chinese indicators
  if (
    h.includes("中国") || h.includes("china") || h.includes("beijing") ||
    h.includes("shanghai") || h.includes("shenzhen") || h.includes("hangzhou") ||
    h.includes("北京") || h.includes("上海") || h.includes("深圳") ||
    h.includes("杭州") || h.includes("广州") || h.includes("成都") ||
    h.includes("腾讯") || h.includes("阿里") || h.includes("百度") ||
    h.includes("字节") || h.includes("华为") || h.includes("小米") ||
    h.includes("京东") || h.includes("美团") || h.includes("滴滴") ||
    h.includes("哔哩") || h.includes("网易") || h.includes("新浪") ||
    h.includes("微博") || h.includes("微信") || h.includes("抖音") || h.includes("快手")
  ) {
    result.push("cn");
  }

  // EU indicators
  if (
    h.includes("germany") || h.includes("france") || h.includes("netherlands") ||
    h.includes("ireland") || h.includes("luxembourg") || h.includes("eu") ||
    h.includes("europe") || h.includes("欧盟") || h.includes("欧洲") ||
    h.includes("德国") || h.includes("法国") || h.includes("荷兰") ||
    h.includes("爱尔兰") || h.includes("amsterdam") || h.includes("berlin") ||
    h.includes("paris") || h.includes("dublin") || h.includes("stockholm") ||
    h.includes("瑞典") || h.includes("丹麦") || h.includes("芬兰")
  ) {
    result.push("eu");
  }

  // US indicators
  if (
    h.includes("美国") || h.includes("united states") || h.includes("america") ||
    h.includes("california") || h.includes("new york") || h.includes("seattle") ||
    h.includes("san francisco") || h.includes("menlo park") || h.includes("cupertino") ||
    h.includes("mountain view") || h.includes("加利福尼亚") || h.includes("纽约") ||
    h.includes("西雅图") || h.includes("旧金山") || h.includes("圣何塞") ||
    h.includes("诺沃克") || h.includes("roseland")
  ) {
    result.push("us");
  }

  return result.length > 0 ? result : ["other"];
}

